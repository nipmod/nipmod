import Link from "next/link";
import { DocsCode, DocsGrid, DocsSection, DocsShell, DocsTable } from "../docs-shell";
import { createPageMetadata } from "../metadata";
import { competitiveBenchmarkReport, type CompetitiveBenchmarkTrack } from "../../lib/competitive-benchmark-public";

export const metadata = createPageMetadata({
  description: "Nipmod competitive benchmark for agent package-decision depth across source metadata, advisories, repository posture and install-plan readiness.",
  path: "/benchmark",
  title: "Nipmod benchmark"
});

export default function BenchmarkPage() {
  const report = competitiveBenchmarkReport;
  const tracks = [...report.tracks].sort((left, right) => right.score - left.score);

  return (
    <DocsShell
      description="A visual readout of the latest production benchmark. It compares the evidence an agent receives before installing, pulling or reusing external code."
      eyebrow="Benchmark"
      stats={[
        { label: "Nipmod live", value: report.headline.liveChecks },
        { label: "Install plans", value: report.headline.installPlanEvidence },
        { label: "Score", value: String(report.headline.score) },
        { label: "Median latency", value: `${report.headline.medianLatencyMs} ms` }
      ]}
      title="Agent package-decision depth."
    >
      <DocsSection eyebrow="Scoreboard" title="Latest production run">
        <div className="benchmark-scoreboard">
          {tracks.map((track) => (
            <BenchmarkRow key={track.name} track={track} />
          ))}
        </div>
      </DocsSection>

      <DocsSection eyebrow="Readout" title="What the numbers mean">
        <DocsGrid>
          <article className="benchmark-panel">
            <span>Public claim</span>
            <strong>{report.publishableClaims[0]}</strong>
            <p>{report.publishableClaims[1]}</p>
          </article>
          <article className="benchmark-panel">
            <span>Boundary</span>
            <strong>Not a universal security ranking.</strong>
            <p>Socket, Snyk, OSV, deps.dev, Scorecard and registries expose different evidence. The benchmark separates those jobs instead of forcing a fake single category.</p>
          </article>
          <article className="benchmark-panel">
            <span>Run</span>
            <strong>Reproducible from the repo.</strong>
            <p>The report is generated by the competitive benchmark CLI and can be rerun before any public article or X thread.</p>
          </article>
        </DocsGrid>
      </DocsSection>

      <DocsSection eyebrow="Claims" title="Safe public wording">
        <DocsTable
          rows={[
            ["Strong", "Nipmod returned read-only install-plan evidence for 7/7 benchmark source cases."],
            ["Strong", "Nipmod's strongest measured dimension is agent preflight: search, inspect, evidence, warnings and install-plan output in one API flow."],
            ["Avoid", "Do not say Nipmod is safer than every competitor."],
            ["Avoid", "Do not say Nipmod guarantees package safety or replaces OSV, deps.dev, Socket, Snyk, OpenSSF or native registries."]
          ]}
        />
      </DocsSection>

      <DocsSection eyebrow="Method" title="How to rerun it">
        <DocsCode>{report.command}</DocsCode>
        <p className="docs-note">
          Last public snapshot: <time dateTime={report.checkedAt}>{formatDate(report.checkedAt)}</time>. Machine report:{" "}
          <Link className="data-link" href="/benchmark.json">/benchmark.json</Link>. Full methodology:{" "}
          <a className="data-link" href="https://github.com/nipmod/nipmod/blob/main/docs/competitive-benchmark.md" rel="noreferrer" target="_blank">docs/competitive-benchmark.md</a>.
        </p>
      </DocsSection>
    </DocsShell>
  );
}

function BenchmarkRow({ track }: { track: CompetitiveBenchmarkTrack }) {
  const latency = track.latencyMs === null ? "n/a" : `${track.latencyMs} ms`;
  return (
    <article className={`benchmark-row benchmark-row-${track.status}`}>
      <div className="benchmark-row-head">
        <div>
          <h3>{track.name}</h3>
          <p>{track.note}</p>
        </div>
        <strong>{track.score}</strong>
      </div>
      <div className="benchmark-bar" aria-label={`${track.name} score ${track.score} out of 100`}>
        <span style={{ width: `${Math.max(2, Math.min(100, track.score))}%` }} />
      </div>
      <dl>
        <div><dt>Coverage</dt><dd>{track.coveragePct}%</dd></div>
        <div><dt>Checks</dt><dd>{track.pass}/{track.applicable}</dd></div>
        <div><dt>Warnings</dt><dd>{track.warn}</dd></div>
        <div><dt>Latency</dt><dd>{latency}</dd></div>
      </dl>
    </article>
  );
}

function formatDate(value: string): string {
  return new Intl.DateTimeFormat("en", {
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    month: "short",
    timeZone: "UTC",
    timeZoneName: "short",
    year: "numeric"
  }).format(new Date(value));
}
